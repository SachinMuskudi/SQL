/* ANSI JOINS */

INNER_JOIN:

SELECT *FROM EMPLOYESS INNER JOIN STUDENT ON EMPLOYESS.EMPID=STUDENT.STDID

LEFT_OUTER_JOIN:
SELECT *FROM EMPLOYESS LEFT OUTER JOIN STUDENT ON EMPLOYESS.EMPID=STUDENT.STDID

RIGHT_OUTER_JOIN:
SELECT *FROM EMPLOYESS RIGHT OUTER JOIN STUDENT ON EMPLOYESS.EMPID=STUDENT.STDID

FULL_OUTER_JOIN:
SELECT *FROM EMPLOYESS FULL OUTER JOIN STUDENT ON EMPLOYESS.EMPID=STUDENT.STDID

CROSS_JOIN:
SELECT *FROM EMPLOYESS CROSS JOIN STUDENT

EQUI_JOIN:

SELECT *FROM EMPLOYESS,STUDENT WHERE EMPLOYESS.EMPID=STUDENT.STDID


NON_EQUI_JOIN:

SELECT *FROM EMPLOYESS,STUDENT WHERE EMPLOYESS.EMPID > STUDENT.STDID

